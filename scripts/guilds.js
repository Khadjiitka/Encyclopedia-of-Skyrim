        const guildList = document.getElementById('guildList');
        const searchInput = document.getElementById('guildSearch');
        const modal = document.getElementById('guildModal');
        const modalBody = document.getElementById('modalBody');

        const revealObserver = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('visible');
                } else {
                    entry.target.classList.remove('visible');
                }
            });
        }, { threshold: 0.1 });

        function initObserver() {
            const items = document.querySelectorAll('.guild-item');
            items.forEach(item => revealObserver.observe(item));
        }

        function renderGuilds(filter = "") {
            guildList.innerHTML = "";
            Object.keys(guildData).forEach(name => {
                if (name.toLowerCase().includes(filter.toLowerCase())) {
                    const data = guildData[name];
                    const item = document.createElement('div');
                    item.className = "guild-item";
                    item.innerHTML = `
                        <img src="${data.img}">
                        <div class="guild-info">
                            <h3>${name}</h3>
                            <p>${data.short}</p>
                        </div>
                    `;
                    item.onclick = () => openModal(name);
                    guildList.appendChild(item);
                }
            });

            initObserver();
        }

        function openModal(name) {
                const data = guildData[name];
                let galleryHTML = data.gallery.map(item => `
        <div class="gallery-card">
            <img src="${item.src}">
            <div class="gallery-card-info">${item.text}</div>
        </div>
    `).join('');
                modalBody.innerHTML = `
        ${data.full}
        <div class="modal-gallery">${galleryHTML}</div>
    `;

                modal.style.display = "block";
                document.body.style.overflow = "hidden";
            }

        document.querySelectorAll('#guildSearch').forEach(input => {
            input.oninput = (e) => renderGuilds(e.target.value);
        });

